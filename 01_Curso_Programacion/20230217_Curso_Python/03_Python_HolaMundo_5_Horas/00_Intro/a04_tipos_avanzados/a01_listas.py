"""Empezamos con las Listas"""
comida = ["Pan", "Aceite", "Carne"]
numeros = [1, 2, 3, 4]

print(numeros)

letras = ["a", "b", "c"]
print(letras)

nombre = ["Chanchito", "Feliz", "Alumnos", "Alberto"]
print(nombre)

booleanos = [True, False, True, True, False]
print(booleanos)

matriz = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(matriz)

ceros = [0] * 10
print(ceros)

unos = [0, 1, 2] * 10
print(unos)

alfanumerico = numeros+letras+nombre+matriz
print(alfanumerico)

rango = list(range(1, 6, 2))
print(rango)

cadena = list("Hola Mundo")
print(cadena)
